const Tour = require('../models/tourModels');

const catchAsync = require('../utils/catchAsync');
const factory = require('./factoryControllers');
const AppError = require('../utils/appError');

const multer = require('multer');
const sharp = require('sharp');
const multerStorage = multer.diskStorage();

const multerFilter = (req, file, cb) => {
  if (file.mimetype.startsWith('image')) cb(null, true);
  cb(new AppError('Not an image! please upload images only', 400), false);
};

//Multer Configuration
const upload = multer({
  storage: multerStorage,
  fileFilter: multerFilter
});
// if you intend to upload:
//NOTE: 1.) 1 image - Use upload.single .. this creates req.file
//NOTE: 1.) Many images - Use upload.fields .. this creates req.files
//NOTE: 1.) Many images with Optionals- Use upload.array .. this creates req.files

exports.uploadTourImages = upload.fields([
  { name: 'imageCover', maxCount: 1 },
  { name: 'images', maxCount: 3 }
]);

exports.resizeTourimages = catchAsync(async (req, res, next) => {
  if (!req.files.imageCover || !req.files.images) return next();
  // 1.) I'll first process the cover image

  req.body.imageCover = `tour-${req.params.id}-${Date.now()}-cover.jpeg`;

  await sharp(req.files.imageCover[0].buffer)
    .resize(2000, 1333)
    .toFormat('jpeg')
    .jpeg({ quality: 90 })
    .toFile(`public/img/tours/${req.body.imageCover}`);

  req.body.images = [];
  await Promise.all(
    req.files.map(async (file, index) => {
      const filename = `tour-${index}-${Date.now()}-cover.jpeg`;

      await sharp(file.buffer)
        .resize(2000, 1333)
        .toFormat('jpeg')
        .jpeg({ quality: 90 })
        .toFile(`public/img/tours/${filename}`);

      req.body.images.push(filename);
    })
  );

  next();
});

exports.aliasTopTours = (req, res, next) => {
  req.query.limit = '5';
  req.query.sort = '-ratingsAverage,price';
  next();
};

